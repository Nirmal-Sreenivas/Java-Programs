package com.loops;

public class pattern3 {
    public static void main(String[] args){
        int n = 5;
        int j;
        int i;
        for(i=1; i<=n; i++) {

            for(j=1; j<=i; j++){
                System.out.print(j);
            }
            System.out.println();


        }
    }
}
