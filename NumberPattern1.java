package com.loops;

public class NumberPattern1 {
    public static void main(String[] args){
        int n = 5;
        int j;
        int i;
        for(j=1; j<=n; j++) {

            for(i=1; i<=n; i++){
                System.out.print("1");
            }
            System.out.println();


        }
    }
}
